function my_callback_fun(hObject, eventdata, handles)
global xtype;
global p
Fs=10000;
N=10000;
dt=1/Fs;
T=N*dt;
t=linspace(0,T,N);
A=get(handles.slider2,'value');
F=get(handles.slider1,'value');
if xtype==1
    x=A*randn(1,N);
    x_1=A*randn(1,N);
end
if xtype==2
    x=A*sin(2*pi*F*t+p*pi/180);
    x_1=A*sin(2*pi*F*t);
end
if xtype==3
    x=A*square(2*pi*F*t+p*pi/180,50);
    x_1=A*square(2*pi*F*t+p*pi/180,50);
end
if xtype==4
    x=A*sawtooth(2*pi*F*t+p*pi/180,0.5);
    x_1=A*sawtooth(2*pi*F*t,0.5);
end
plot(handles.axes1,t(1:N/10),x(1:N/10))
plot_right(hObject, eventdata, handles,x_1,Fs,N)
p=p+10



function plot_right(hObject, eventdata, handles,x,Fs,N)
fmax=max(x);
s3=sprintf('%f',fmax);
set(handles.edit3,'String',s3)
fmin=max(-x);
vpp=fmax+fmin;
s9=sprintf('%f',vpp);
set(handles.edit9,'String',s9)
ff=fft(x);
yff=2*abs(ff(1:N/2))/N;
ft=Fs*(0:N/2-1)/N;
frequency=ft(find(yff==max(yff)));
s12=sprintf('%f',frequency);
set(handles.edit12,'String',s12)
fmean=mean(x(1:Fs/frequency));
s8=sprintf('%f',fmean);
set(handles.edit8,'String',s8)
frms=rms(x(1:Fs/frequency));
s10=sprintf('%f',frms);
set(handles.edit10,'String',s10)
fstd=std(x(1:Fs/frequency));
s11=sprintf('%f',fstd);
set(handles.edit11,'String',s11)
